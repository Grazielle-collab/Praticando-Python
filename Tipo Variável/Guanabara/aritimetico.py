n1 = int(input('Digite um número:'))
# O input foi jogado dentro da variavel n1 que esse n1 ira receber um resultado de um input convertido por um número inteiro
n2 = int(input('Digite mais um número:'))
# n2 ira receber o resultado de um input convertido por numero inteiro 
s = n1 + n2 
# A variavel S vai receber n1 + n2 que será somado e jogado no resultado dentro do S 
print('A soma vale()'.format (s))
# No final escrevo o valor de S formatado na substituição da mascara () dentro do print
